import numpy as np
import tensorflow as tf
import re
import requests

from tensorflow.keras.models import Sequential
from tensorflow.keras.layers import Embedding, LSTM, GRU, Dense
from tensorflow.keras.preprocessing.text import Tokenizer
from tensorflow.keras.preprocessing.sequence import pad_sequences

# ===============================
# 2. LOAD DATASETS
# ===============================
def load_text(path, limit=200000):
    with open(path, "r", encoding="utf-8") as f:
        text = f.read()[:limit].lower()
    text = re.sub(r"[^a-z\s]", "", text)
    return text

# 👉 Update paths if needed
shakespeare_text = load_text("/kaggle/input/datasets/madhinikamalaguru/ts-and-sh/tiny-shakespeare.txt")
sherlock_text = load_text("/kaggle/input/datasets/madhinikamalaguru/ts-and-sh/Sherlock Holmes.txt")

# ===============================
# 3. TOKENIZER
# ===============================
tokenizer = Tokenizer(num_words=5000)
tokenizer.fit_on_texts([shakespeare_text, sherlock_text])

total_words = min(5000, len(tokenizer.word_index) + 1)

# ===============================
# 4. SEQUENCE CREATION
# ===============================
def create_sequences(text):
    sequences = []
    for line in text.split("\n")[:3000]:
        tokens = tokenizer.texts_to_sequences([line])[0]
        for i in range(2, len(tokens)):
            sequences.append(tokens[:i])
    return sequences

seq1 = create_sequences(shakespeare_text)
seq2 = create_sequences(sherlock_text)

all_sequences = seq1 + seq2

max_len = max(len(seq) for seq in all_sequences)
all_sequences = pad_sequences(all_sequences, maxlen=max_len, padding="pre")

X = all_sequences[:, :-1]
y = tf.keras.utils.to_categorical(all_sequences[:, -1], num_classes=total_words)

print("Training samples:", X.shape[0])

# ===============================
# 5. BUILD MODELS
# ===============================
def build_lstm():
    model = Sequential([
        Embedding(total_words, 64, input_length=max_len-1),
        LSTM(128),
        Dense(total_words, activation="softmax")
    ])
    model.compile(loss="categorical_crossentropy", optimizer="adam", metrics=["accuracy"])
    return model

def build_gru():
    model = Sequential([
        Embedding(total_words, 64, input_length=max_len-1),
        GRU(128),
        Dense(total_words, activation="softmax")
    ])
    model.compile(loss="categorical_crossentropy", optimizer="adam", metrics=["accuracy"])
    return model

# ===============================
# 6. TRAIN MODELS
# ===============================
lstm_model = build_lstm()
gru_model = build_gru()

print("Training LSTM...")
lstm_model.fit(X, y, epochs=5, batch_size=64, verbose=1)

print("Training GRU...")
gru_model.fit(X, y, epochs=5, batch_size=64, verbose=1)

# ===============================
# 7. TOP-3 ACCURACY
# ===============================
def top_k_accuracy(model, X, y, k=3, samples=500):
    correct = 0

    for i in range(min(samples, len(X))):
        preds = model.predict(X[i:i+1], verbose=0)[0]
        top_k = np.argsort(preds)[-k:]

        true_word = np.argmax(y[i])

        if true_word in top_k:
            correct += 1

    return correct / min(samples, len(X))

print("\nEvaluating Models...")

lstm_acc = top_k_accuracy(lstm_model, X, y, k=3)
gru_acc = top_k_accuracy(gru_model, X, y, k=3)

print("\nTop-3 Accuracy:")
print("LSTM:", round(lstm_acc, 4))
print("GRU :", round(gru_acc, 4))

