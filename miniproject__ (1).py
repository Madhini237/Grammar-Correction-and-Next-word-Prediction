def run_system(sentence):
    print("\nInput:", sentence)

    corrected = grammar_correct(sentence)
    print("Corrected:", corrected)

    lstm_word, lstm_conf = predict_next(lstm_model, corrected)
    gru_word, gru_conf = predict_next(gru_model, corrected)

    print("\n--- LSTM ---")
    print("Prediction:", lstm_word)
    print("Confidence:", round(lstm_conf, 4))

    print("\n--- GRU ---")
    print("Prediction:", gru_word)
    print("Confidence:", round(gru_conf, 4))

    if lstm_conf > gru_conf:
        print("\nFinal Selected Model: LSTM")
    else:
        print("\nFinal Selected Model: GRU")

